/** Normalize name for exact match: trim, collapse spaces, lower case */
export function normalizeNameForMatch(name) {
  if (typeof name !== 'string') return '';
  return name.trim().replace(/\s+/g, ' ').toLowerCase();
}

/** Find verified attendee by exact name match. Returns { name, email } or null */
export function findVerifiedByExactName(searchName) {
  const key = normalizeNameForMatch(searchName);
  if (!key) return null;
  return CERTVAULT_VERIFIED_ATTENDEES.find(
    (a) => normalizeNameForMatch(a.name) === key
  ) || null;
}
